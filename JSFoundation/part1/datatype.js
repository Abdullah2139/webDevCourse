// datatypes

// 1. Number
let num = 10; // integer
let num2 = 10.5; // float
let num3 = 10e5; // scientific notation

// 2. String
let str = "Hello World"; // double quotes

// 3. Boolean
let bool = true; // true or false

// 4. Undefined
let und; // variable declared but not initialized

// 5. Null
let n = null; // variable explicitly set to null

// 6. Symbol (ES6)
let sym = Symbol("description"); // unique and immutable value

